# Some of the common data types in python

# String
print("Hello")
# -- print the data type of the literal above --
print(type("Hello"))

# Integer
print(123)
# -- print the data type of the literal above --
print(type(123))

# Float
print(3.14159)
# -- print the data type of the literal above --
print(type(3.14159))

# Boolean
print(True)
print(False)
# -- print the data type of the literals above --
print(type(True))
print(type(False))